package in.fiinfra.common.utility;

public class SectionData {

	int goalCount;
	String retirmentAge;
	String cashflowIncomeValue;
	String cashflowExpenditureValue;
	String emergencyIncomeOption;
	int count;
	int isPersonalInfoFilled;
	public int getGoalCount() {
		return goalCount;
	}
	public void setGoalCount(int goalCount) {
		this.goalCount = goalCount;
	}
	public String getRetirmentAge() {
		return retirmentAge;
	}
	public void setRetirmentAge(String retirmentAge) {
		this.retirmentAge = retirmentAge;
	}
	public String getCashflowIncomeValue() {
		return cashflowIncomeValue;
	}
	public void setCashflowIncomeValue(String cashflowIncomeValue) {
		this.cashflowIncomeValue = cashflowIncomeValue;
	}
	public String getCashflowExpenditureValue() {
		return cashflowExpenditureValue;
	}
	public void setCashflowExpenditureValue(String cashflowExpenditureValue) {
		this.cashflowExpenditureValue = cashflowExpenditureValue;
	}
	public String getEmergencyIncomeOption() {
		return emergencyIncomeOption;
	}
	public void setEmergencyIncomeOption(String emergencyIncomeOption) {
		this.emergencyIncomeOption = emergencyIncomeOption;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getIsPersonalInfoFilled() {
		return isPersonalInfoFilled;
	}
	public void setIsPersonalInfoFilled(int isPersonalInfoFilled) {
		this.isPersonalInfoFilled = isPersonalInfoFilled;
	}
	
	
}
