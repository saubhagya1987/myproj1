package in.fiinfra.common.service.vo;

public class Entities {

	private String duration;
	private int oppnumbers;
	private int clientnumbers;
	private int premiumnumbers;
	private int folionumbers;
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public int getOppnumbers() {
		return oppnumbers;
	}
	public void setOppnumbers(int oppnumbers) {
		this.oppnumbers = oppnumbers;
	}
	public int getClientnumbers() {
		return clientnumbers;
	}
	public void setClientnumbers(int clientnumbers) {
		this.clientnumbers = clientnumbers;
	}
	public int getPremiumnumbers() {
		return premiumnumbers;
	}
	public void setPremiumnumbers(int premiumnumbers) {
		this.premiumnumbers = premiumnumbers;
	}
	public int getFolionumbers() {
		return folionumbers;
	}
	public void setFolionumbers(int folionumbers) {
		this.folionumbers = folionumbers;
	}
	
	
}
