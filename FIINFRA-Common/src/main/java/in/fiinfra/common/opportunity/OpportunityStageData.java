package in.fiinfra.common.opportunity;

public class OpportunityStageData {
	private String fpStageEndDate;
	private String fpStageEndDateTime;
	private int partyId;
	private String stage;
	public String getFpStageEndDate() {
		return fpStageEndDate;
	}
	public void setFpStageEndDate(String fpStageEndDate) {
		this.fpStageEndDate = fpStageEndDate;
	}
	public int getPartyId() {
		return partyId;
	}
	public void setPartyId(int partyId) {
		this.partyId = partyId;
	}
	public String getStage() {
		return stage;
	}
	public void setStage(String stage) {
		this.stage = stage;
	}
	public String getFpStageEndDateTime() {
		return fpStageEndDateTime;
	}
	public void setFpStageEndDateTime(String fpStageEndDateTime) {
		this.fpStageEndDateTime = fpStageEndDateTime;
	}
	
	
}
