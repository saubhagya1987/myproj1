package in.fiinfra.common.service.vo;

public class DataCollection {
	private String duration;
	private int dataCollectionActivated;
	private int dataCollectionCompleted;
	private int opportunities;
	private int goals;
	private String goalAmount;
	private String investmentAmount;
	private String potentialRevenue;
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public int getDataCollectionActivated() {
		return dataCollectionActivated;
	}
	public void setDataCollectionActivated(int dataCollectionActivated) {
		this.dataCollectionActivated = dataCollectionActivated;
	}
	public int getDataCollectionCompleted() {
		return dataCollectionCompleted;
	}
	public void setDataCollectionCompleted(int dataCollectionCompleted) {
		this.dataCollectionCompleted = dataCollectionCompleted;
	}
	public int getOpportunities() {
		return opportunities;
	}
	public void setOpportunities(int opportunities) {
		this.opportunities = opportunities;
	}
	public int getGoals() {
		return goals;
	}
	public void setGoals(int goals) {
		this.goals = goals;
	}

	public String getInvestmentAmount() {
		return investmentAmount;
	}
	public void setInvestmentAmount(String investmentAmount) {
		this.investmentAmount = investmentAmount;
	}
	public String getGoalAmount() {
		return goalAmount;
	}
	public void setGoalAmount(String goalAmount) {
		this.goalAmount = goalAmount;
	}
	public String getPotentialRevenue() {
		return potentialRevenue;
	}
	public void setPotentialRevenue(String potentialRevenue) {
		this.potentialRevenue = potentialRevenue;
	}

	
	
}
